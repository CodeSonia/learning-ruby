=begin
    
The partner to the if statement is the else statement.

An if/else statement says to Ruby: "If this expression is true,
run this code block; otherwsise, run the code after the else statement"
    
=end

if 10 > 5
  puts "I'm being printed because 10 is greater than 5!"
else
  puts "I'm being printed because I am less than 5"
end

# I'm being printed because 10 is greater than 5!
