=begin
    
Ruby's if statement takes an expression (something that has a value),
that evaluated to either true or false. 

If that expression is true, Ruby executes the block of code that follows the if.
If it's not true, ruby doesn't execute that block of code - it skips that block
and goes to another. 

Note that Ruby doesn't care about whitespaces, indentation doesn't matter,
but for readability indenting the print statement or code block inside the if
looks better. 
    
=end

if 20 > 10
    print "You're number is over 10! Hurrah!"
end

# You're number is over 10! Hurrah! 

